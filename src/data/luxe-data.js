export function formatVnd(amount) {
  try {
    return new Intl.NumberFormat(appConfig.defaultLocale, {
      style: 'currency',
      currency: appConfig.currency,
      maximumFractionDigits: 0,
    }).format(amount)
  } catch {
    return `${amount} ${appConfig.currency}`
  }
}

export function findProduct(productId) {
  return products.find((p) => p.id === productId) ?? null
}

export function findCategory(categoryId) {
  return categories.find((item) => item.id === categoryId) ?? null
}

export function productsByCategory(categoryId) {
  return products.filter((item) => item.categoryId === categoryId)
}

export function mixProductsForCategory(categoryId, count = 6) {
  const categoryProducts = productsByCategory(categoryId)
  const mixed = [...categoryProducts, ...products.filter((product) => product.categoryId !== categoryId)]
  return mixed.slice(0, count)
}

